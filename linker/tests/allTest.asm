SECTION TEXT
ADD DOIS
SUB UM
STORE OLD_DATA
LOAD DOIS
STORE OLD_DATA + 1
LOAD TRES
STORE OLD_DATA + 2
LOAD DOIS
MULT DOIS
STORE OLD_DATA + 3
MULT TRES
DIV DOIS
STORE OLD_DATA + 4
JMP L1

L2:
OUTPUT OLD_DATA + 2
LOAD UM
JMPP L3

L4:
COPY CINCO, OLD_DATA + 4
OUTPUT OLD_DATA + 4
STOP

LN:
C_INPUT INPUTS_DATA
C_OUTPUT INPUTS_DATA
H_INPUT INPUTS_DATA
H_OUTPUT INPUTS_DATA
S_INPUT INPUTS_DATA, 50
S_OUTPUT INPUTS_DATA, 50

L1:
OUTPUT OLD_DATA
OUTPUT OLD_DATA + 1
LOAD MENOS_UM
JMPN L2

L3:
OUTPUT OLD_DATA + 3
LOAD ZERO
JMPZ L4

SECTION DATA
MENOS_UM: CONST -1
ZERO: CONST 0
UM: CONST 1
DOIS: CONST 2
TRES: CONST 3
CINCO: CONST 5
OLD_DATA: SPACE 5
INPUTS_DATA: SPACE 100
